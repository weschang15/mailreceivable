const timeoutDefault = time.Second * 10

// Validate attempts to check the syntax of a provided email address.
// It returns an error if the addrss is formatted incorrectly.
func Validate(e string) error {
	if !emailRegexp.MatchString(strings.ToLower(e)) {
		return ErrInvalidEmail
	}

	return nil
}

// VerifyMX checks if the MX DNS records exist for the given domain
func VerifyMX(e string) error {
	_, host := split(e)
	if _, err := net.LookupMX(host); err != nil {
		return ErrHostNotFound
	}

	return nil
}

// VerifyHost attempts to connect to the host associated with a provided email address.
// It returns an error if the connection fails.
func VerifyHost(e string) error {
	_, host := split(e)
	mx, err := net.LookupMX(host)

	if err != nil {
		return ErrHostNotFound
	}

	address := fmt.Sprintf("%s:%d", mx[0].Host, 25)
	client, err := DialClient(address, timeoutDefault)

	if err != nil {
		return err
	}

	client.Close()
	return nil
}

func DialClient(a string, t time.Duration) (*smtp.Client, error) {
	conn, err := net.DialTimeout("tcp", a, t)
	if err != nil {
		return nil, err
	}

	timer := time.AfterFunc(t, func() { conn.Close() })
	defer timer.Stop()

	host, _, _ := net.SplitHostPort(a)
	return smtp.NewClient(conn, host)
}

// Split extracts the email user account and email host from a provided address.
// It returns both the user and host
func split(e string) (user, host string) {
	index := strings.LastIndexByte(e, '@')
	if index < 0 {
		return
	}

	user = e[:index]
	host = e[index+1:]

	return
}
